function normalizeModuleLevel(value) {
  const raw = String(value || "").toLowerCase();
  if (raw.includes("debut")) return "Debutant";
  if (raw.includes("avanc")) return "Avance";
  return "Intermediaire";
}

function listModuleTopics() {
  return MODULE_TOPICS.map((topic) => ({
    slug: topic.slug,
    title: topic.title,
    category: topic.category,
    summary: topic.summary,
    customerProfile: topic.customerProfile,
    clientOpening: topic.clientOpening,
    successCriteria: topic.successCriteria,
    roleplayNotes: topic.roleplayNotes,
    coachingFocus: topic.coachingFocus
  }));
}

function getModuleTopicBySlug(slug) {
  const target = String(slug || "").trim().toLowerCase();
  return MODULE_TOPICS.find((topic) => topic.slug === target) || null;
}

